function getPais(nomenclatura) {
  const paises = {
    ARG: "Argentina",
    BRA: "Brazil",
    CHI: "Chile",
    COL: "Colombia",
    ECU: "Ecuador",
    PAR: "Paraguay",
    PER: "Peru",
    URU: "Uruguay",
    VEN: "Venezuela",
    PA: "Panama"
  };
  
  salida = paises[nomenclatura] 
  return salida;
}